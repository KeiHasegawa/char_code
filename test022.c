#include <stdio.h>

int main()
{
  char a[] = {
    0x2f,0x2a,0x0a,0x20,0x2a,0x20,  // C スタイルのコメントの開始

    0x1b,0x24,0x42,  // 漢字の始まり
    0x3e,0x72,0x37,0x6f,0x24,0x72,   // 条件を

    0x1b,0x28,0x49,  // 半角カナの始まり
    0x41,0x2a,0x2f,0x38,  // ﾁｪｯｸ

    0x1b,0x24,0x42,  // 漢字の始まり
    0x24,0x39,0x24,0x6b,  // する

    0x1b,0x28,0x42,  // ASCII の始まり
    0x0a,0x20,0x2a,0x2f,0x0a  // C スタイルのコメントの終わり
  };
  for (int i = 0 ; i != sizeof a/sizeof a[0] ; ++i)
    putc(a[i], stdout);
  return 0;
}
